package com.xlh.crawler.dto;

public class PrepertyRightInfo {
    private String enterpriseName;
    private String rightType;
    private String rightNo;
    private String rightName;
    private String applicant;
    private String filingDate;
    private String mainClassNo;

    public String getEnterpriseName() {
        return enterpriseName;
    }

    public void setEnterpriseName(String enterpriseName) {
        this.enterpriseName = enterpriseName;
    }

    public String getRightType() {
        return rightType;
    }

    public void setRightType(String rightType) {
        this.rightType = rightType;
    }

    public String getRightNo() {
        return rightNo;
    }

    public void setRightNo(String rightNo) {
        this.rightNo = rightNo;
    }

    public String getRightName() {
        return rightName;
    }

    public void setRightName(String rightName) {
        this.rightName = rightName;
    }

    public String getApplicant() {
        return applicant;
    }

    public void setApplicant(String applicant) {
        this.applicant = applicant;
    }

    public String getFilingDate() {
        return filingDate;
    }

    public void setFilingDate(String filingDate) {
        this.filingDate = filingDate;
    }

    public String getMainClassNo() {
        return mainClassNo;
    }

    public void setMainClassNo(String mainClassNo) {
        this.mainClassNo = mainClassNo;
    }
}
